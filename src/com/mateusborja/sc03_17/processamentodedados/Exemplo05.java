package com.mateusborja.sc03_17.processamentodedados;

public class Exemplo05 {

	public static void main(String[] args) {

		double a;
		int b;

		double c;
		int d;

		a = 5.0;
		b = (int) a;

		c = 4.0;
		d = (int) c; //casting

		System.out.println(a);
		System.out.println(b);

		System.out.println(c);
		System.out.println(d);

	}

}
