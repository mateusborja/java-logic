package com.mateusborja.sc05_45.testedemesa;

public class TesteMesaWhile07 {

	public static void main(String[] args) {

		int x = 4, y = 0, i = 0;

		while (i < x) {
			i = i + 1;
			y = y + 1;
			System.out.print(i);
			System.out.println(y);
		}

		System.out.println("\n");

	}

}
