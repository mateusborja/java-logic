package com.mateusborja.sc04_32.expressoescomparativas;

public class ComparacaoUm {

	@SuppressWarnings("unused")
	public static void main(String[] args) {

		int x = 5;

		if (x > 0) {
			System.out.println("Resultado Verdadeiro" + " x > 0");

		} else
			System.out.println("Resultado Falso" + "x > 0");
		
		if (x == 3) {
			System.out.println("Resultado Verdadeiro" + " x == 3");
			
		} else
			System.out.println("Resultado Falso" + " x == 3");
		
		if (10 <= 30) {
			System.out.println("Resultado Verdadeiro" + " 10 <= 30");
			
		} else
			System.out.println("Resultado Falso" + " 10 <= 30");
		
		if (x != 2) {
			System.out.println("Resultado Verdadeiro" + " x != 2");
			
		} else
			System.out.println("Resultado Falso" + " x != 2");
		


	}

}
